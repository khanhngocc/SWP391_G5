package model;

/**
 *
 * @author dell
 */
public class AccountForgot {
    
    private int id;
    private int user_id;
    private String allowedReset;
    private String timeStarted;
    private String timeEnded;
    private String dateCreated;

    public String getDateCreated() {
        return dateCreated;
    }

    public void setDateCreated(String dateCreated) {
        this.dateCreated = dateCreated;
    }
    
    

    public String getTimeStarted() {
        return timeStarted;
    }

    public void setTimeStarted(String timeStarted) {
        this.timeStarted = timeStarted;
    }

    public String getTimeEnded() {
        return timeEnded;
    }

    public void setTimeEnded(String timeEnded) {
        this.timeEnded = timeEnded;
    }
    
    
    
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getUser_id() {
        return user_id;
    }

    public void setUser_id(int user_id) {
        this.user_id = user_id;
    }

    public String getAllowedReset() {
        return allowedReset;
    }

    public void setAllowedReset(String allowedReset) {
        this.allowedReset = allowedReset;
    }
    
    
}
